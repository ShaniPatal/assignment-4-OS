char*
fmtname(char *path)
{
  static char buf[DIRSIZ+1];
  char *p;
  char buffie[DIRSIZ];
  int read_link_ret = readlink(path, buf, DIRSIZ);
  // printf("%d", read_link_ret);

  // Find first character after last slash.
  for(p=path+strlen(path); p >= path && *p != '/'; p--)
    ;
  p++;

  // Return blank-padded name.
  if(strlen(p) >= DIRSIZ)
    return p;
  memmove(buf, p, strlen(p));
  
  int symname = strlen(p);
  if(read_link_ret == 0){//if the path is to a symlink
  // printf("27");
    memmove(buf + symname, "->", 2);
      // printf("28");

    symname+=2;
      // printf("29");

    memmove(buf + symname, buffie, strlen(buffie));
      // printf("30");

    symname+=strlen(buffie);
      // printf("31");

  }

  memset(buf+strlen(p), ' ', DIRSIZ-strlen(p));
    // printf("32");

  return buf;
}


void
ls(char *path)
{
  char buf[512], *p;
  int fd;
  struct dirent de;
  struct stat st;

  if((fd = open(path, 0)) < 0){
    fprintf(2, "ls: cannot open %s\n", path);
    printf("AAAAAAAAAAAAA\n");

    return;
  }

  if(fstat(fd, &st) < 0){
    fprintf(2, "ls: cannot stat %s\n", path);
    close(fd);
    return;
  }

  switch(st.type){

  case T_SYMLINK:
    printf("%s %d %d %l\n", fmtname(buf), st.type, st.ino, st.size);
    break;
    
  case T_FILE:
    printf("%s %d %d %l\n", fmtname(path), st.type, st.ino, st.size);
    break;

  case T_DIR:
    if(strlen(path) + 1 + DIRSIZ + 1 > sizeof buf){
      printf("ls: path too long\n");
      break;
    }
    strcpy(buf, path);
    p = buf+strlen(buf);
    *p++ = '/';
    while(read(fd, &de, sizeof(de)) == sizeof(de)){
      if(de.inum == 0)
        continue;
      memmove(p, de.name, DIRSIZ);
      p[DIRSIZ] = 0;
      if(stat(buf, &st) < 0){
        printf("ls: cannot stat %s\n", buf);
        continue;
      }
      printf("%s %d %d %d\n", fmtname(buf), st.type, st.ino, st.size);
    }
    break;
  }
  close(fd);
}

int
main(int argc, char *argv[])
{
  int i;

  if(argc < 2){
    ls(".");
    exit(0);
  }
  for(i=1; i<argc; i++)
    ls(argv[i]);
  exit(0);
}
